  while (rowToCheck < arr.length && arr[rowToCheck][colToCheck] == '.') {
    rowToCheck += 1;
  }
  if (rowToCheck < arr.length && arr[rowToCheck][colToCheck] == '#') {
    count++;
  }

  rowToCheck = row + 1;
  colToCheck = col + 1;
  while (rowToCheck < arr.length && colToCheck < arr[0].length && arr[rowToCheck][colToCheck] == '.') {
    rowToCheck += 1;
    colToCheck += 1;
  }
  if (rowToCheck < arr.length && colToCheck < arr[0].length && arr[rowToCheck][colToCheck] == '#') {
    count++;
  }


  // console.log(`count: ${count}`);
  return count;
